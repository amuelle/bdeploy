package io.bdeploy.interfaces.manifest.banner;

public class InstanceBannerRecord {

    /**
     * The foreground color, e.g. #000000
     */
    public String foregroundColor;

    /**
     * The background color, e.g. #FF0000
     */
    public String backgroundColor;

    /**
     * The banner text
     */
    public String text;

    /**
     * The user who set this banner
     */
    public String user;

    /**
     * Timestamp when this banner was set
     */
    public long timestamp;

}
